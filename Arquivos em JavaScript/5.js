var x = parseFloat(prompt("Digite um número"));
var y = parseFloat(prompt("Digite um número"));
        

if (x == y) {
    document.write("Os números são iguais!!!");
}

else if (x > y) {
    document.write("O primeiro número é maior!!!");
}

else if (x < y) {
    document.write("O segundo número é maior!!!");
}
